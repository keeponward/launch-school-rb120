# Create an object-oriented number guessing class for numbers in the range 1 to 100, with a limit of 7 guesses per game. The game should play like this:

class GuessingGameMine
  INITIAL_NUM_GUESSES = 7
  GUESS_RANGE = (1..100)

  attr_accessor :num_guesses, :random_selection

  # def initialize
  #   @num_guesses = INITIAL_NUM_GUESSES
  # end

  def play
    self.num_guesses = INITIAL_NUM_GUESSES
    self.random_selection = rand(GUESS_RANGE)

    puts "random_selection = #{random_selection}"

    loop do     # until player guess the number or runs out of guesses

      if num_guesses == 1
        puts 'You have 1 guess remaining.'
      else
        puts "You have #{num_guesses} guesses remaining."
      end

      player_guess = nil
      loop do
        print "Enter a number between 1 and 100: "
        player_guess = gets.chomp.to_i
        break if GUESS_RANGE.include? player_guess
        print "Invalid guess. "
      end

      if player_guess < random_selection
        puts "Your guess is too low."
      elsif player_guess > random_selection
        puts "Your guess is too high."
      else
        puts "That's the number!"
        break
      end
      self.num_guesses -= 1
      if num_guesses == 0
        puts "You have no more guesses. You lost!"
        break
      end
    end
  end   # play method

end

